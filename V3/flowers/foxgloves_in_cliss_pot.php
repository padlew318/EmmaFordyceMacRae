<p>Emma Fordyce MacRae records no exhibition history for this painting.  Her record states she started the painting with its foxgloves of white and pale pink and a background of yellow ochre in July 1935 and finished it in 1937. She records two other paintings with foxgloves in 1932, one of which received Honorable Mention at the Allied Artists of America exhibition in 1932.  The painting is quite different to the earlier Cosmos Decoration with the pale pink of the foxglove repeated in the Quimper figures, through the decoration in the background and the flowers on the pot unifying the whole.</p>
