                    Emma Fordyce MacRae started to draw when she was very young often illustrating letters to her mother with flowers and is known for her still lives of dogwood, lilies, irises and other flowers.  
					She often included a bowl of flowers in her figure paintings and interior scenes.   
					In painting of flowers <em>Gloucester Garden</em> she has painted the flowers in the garden of Atlantic Highlands and this painting of flowers may be considered earlier than some of the paintings of landscapes seen today.  
					Emma has not scraped the paint back.
                  </p>
