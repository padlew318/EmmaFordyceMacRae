                  <p>
                    Emma’s husband Homer designed the studio at Atlantic Highlands.  The house today has been altered and added to and is much changed. Other houses have been built nearby.  
					However, a lady I spoke to knew the house well and had a wonderful recollection of Emma’s studio in the house from the years she visited her grandparents who lived on Western Avenue during holidays 
					and described how once she climbed through a window left open by Emma at the end of the summer.
                  </p>
				  <p>There she found a painting on the easel, paints open, paintings stacked against the walls and a wonderful chaise longue she and her friends lay on pretending to be important.  
				  Some years later in 1967 she purchased the house and the studio was just as she remembered it. This photograph was taken in 2008.</p>
