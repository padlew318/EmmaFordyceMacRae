         <p>
                    Half Moon Beach can be reached on foot from the parking area by following the path which will take you to the beach which has not changed since Emma painted her <em>Half Moon Beach.</em>
                  </p>
                  <p>
                    As you continue along Route 127 which is now Western Avenue to the Stage Fort Park parking lot you will drive past the sculpture Man at the Wheel in memory of the many fishermen who have lost their lives at sea and after passing over Blyman Drawbridge, raised to allow boats to enter the Annisquam River, there is another memorial to their wives.  Once over the drawbridge make a slight left into Hough Avenue and the Stage Fort Park parking area.
                  </p>
                  <p>
                    The drawbridge was the only vehicle access before the modern bridge was built connecting the town to the mainland.  Emma would probably have arrived in Gloucester by train from Boston.
                  </p>
                  <p>
                    One of Emma’s teachers at the Art Students League was John Sloan who also came in the summer to paint and rented a house.  Other influences are reflected in her collection of art books.  As most students at this time Emma had a copy of Arthur Dow’s <em>Composition</em> as well as books on Gauguin and Cezanne in her library.
                  </p>
                  <p>
                    Emma avoided the open vista of the ocean. Other contemporary painters working on Cape Ann also eschewed shore subjects, including John Sloan, Edward Hopper and Marsden Hartley, but they completely ignored the coast, not depicting even the part of the beaches that Emma featured in her landscapes.  In <em>Half Moon Beach</em> although the beach is hemmed in she has included a distant view of the ocean.  As often in her landscapes such as <em>Pigeon Cove</em> there appears to be little movement but closer examination reveals the swimmers and the boy taking off his shoe and the reflection in the water of the pine trees and swimmers. 
                  </p>
