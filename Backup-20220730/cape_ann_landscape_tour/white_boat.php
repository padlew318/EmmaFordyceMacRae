                  <p>
                    Emma’s original oil paintings had been chosen by the Atwoods to be exhibited and hung at the Gallery-on-the-Moors, the first collective exhibition space on Cape Ann and  forerunner to the North Shore Arts Association which opened in 1922.  The North Shore Arts Association had and still has a jury selecting the paintings in contrast to the Atwoods who as owners of the gallery made the selection.  The <em>White Boat</em> painted in 1924, four years after her ten year “marriage” was dissolved and annulled in Arizona, was her first painting selected to be exhibited by the North Shore Arts Association jury and was exhibited in 1925.  Emma continued to exhibit annually with the North Shore Arts Association until 1958. 
                  </p>
                  <p>
                    From Rockport head back to Gloucester and Commercial Street where Emma captured the scene of the <em>White Boat</em> setting up her easel overlooking the Inner Harbour adjacent to Fort Square and somewhere near the old Cunningham and Thompson Fish Wharf which was at 88 Commercial Street in 1925.   The sailing boat is tied up at the covered open bays of the Atlantic Supply Co. owned at the time Emma painted <em>White Boat</em> by Captain Ben Pine.   The three towers in the background belong to the Baptist Church on Pleasant Street, and the Unitarian Church and Trinity Church on Middle Street.  The domed towers of City Hall can also be seen in the background.  The red brick buildings are on Main Street.
