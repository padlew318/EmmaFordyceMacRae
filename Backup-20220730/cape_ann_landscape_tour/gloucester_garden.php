                  <p>
                    Emma Fordyce MacRae started to draw when she was very young often illustrating letters to her mother with flowers and is known for her still lives of dogwood, lilies, irises and other flowers.  She often included a bowl of flowers in her figure paintings and interior scenes.   In painting of flowers <em>Gloucester Garden</em> she has painted the flowers in the garden of Atlantic Highlands and this painting of flowers may be considered earlier than some of the paintings of landscapes seen today.  Emma has not scraped the paint back.
                  </p>
                  <p>
                    Emma’s husband Homer designed the studio at Atlantic Highlands.  The house today has been altered and added to and is much changed. Other houses have been built nearby.  However, a lady I spoke to knew the house well and had a wonderful recollection of Emma’s studio in the house from the years she visited her grandparents who lived on Western Avenue during holidays and described how once she climbed through a window left open by Emma at the end of the summer.
                  </p>
