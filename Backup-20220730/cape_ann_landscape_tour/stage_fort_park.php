                    Emma abandoned by MacRae may have lived with her parents during the summers at their home on Old Salem Road while waiting for her marriage to end.  Emma’s father purchased the land in 1916 and built Atlantic Highlands which he left to Emma when he died in 1925 -- the year she exhibited <em>White Boat.</em>
                  </p>
