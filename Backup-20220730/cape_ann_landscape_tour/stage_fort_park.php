                    From here Emma painted the landscape with flowers in the garden, the tombstones in the cemetery, the American flag, the Annisquam River and the town in the distance.  Her technique in <em>Stage Fort Park</em> is similar to her other paintings but without so much of the background showing through and in this landscape oil painting there are no boats or reflections in the water.
