				  <p>The original oil painting with fishermen and their boats reflected in the water beside the dock painted by the 20th century American artist Emma Fordyce MacRae is of Pigeon Cove harbor</p>
                  <p>
                    Emma often came to Pigeon Cove harbor.  A large warehouse built at the entrance after Emma left Gloucester obscuring the view of the harbor was torn down in around 2008 and today two fishermen’s huts on the dock with the granite blocks piled high behind them remain from Emma’s day and can be seen in her <em>Fishermen at Pigeon’s Cove</em> painted in the evening with the boats returning at low tide.   Emma set up her easel on the dock and the landscape looks back along the dock and includes boats, the two fishermen’s huts and the house without the widow’s walk which can be today.
                  </p>
                  <p>
                    Leaving Lanes Cove continue northwards on Washington Street or Route 127 you will pass Folly Cove where Ellen Day Hale, a contemporary of Emma’s lived, as well as Gabriele de Veaux Clements, and Ellen’s brother Phillip and wife Lillian Westcott Hale.   Route 127 for reasons of its own becomes Granite Street but remains Route 127 and the Pigeon Cove harbor area is at the bottom of the hill at sea level.  Turn onto Breakwater Avenue.
                  </p>
                  <p>
                    From her years at art school Emma learned a love of the mechanics of painting, the importance of preparing a canvas or board for oil painting, the choice of texture and the role of the brush and palette knife.  In <em>Fishermen at Pigeon Cove</em> Emma’s reflections in the water reflect the sky and the under painting is a yellow tone with other colors built up with a brush or palette knife one on top of another to create the depth and dimension of what seems at first glance to be a twilight tranquil scene but which is, in fact, bustling with activity.  In this landscape painting once again can be seen the white under painting used to good effect to highlight around roofs and people and even the stones forming the dock. 
                  </p>
